        #region EncodedActionLink
        public MvcHtmlString EncodedActionLink(string linkText, string baseUrl)
        {
            //<a href="/Answer?questionId=14">What is Entity Framework??</a>
            System.Text.StringBuilder ancor = new System.Text.StringBuilder();
            ancor.Append("<a ");
            ancor.Append(" href='");

